/// The type of the game status.
enum PlayStatusType {
  /// Home overlay of the game.
  home,

  /// Game is currently playing.
  playing,

  /// Score overlay after game over.
  score,

  /// Ranking overlay.
  ranking,

  /// Player details overlay.
  player,

  /// Settings overlay.
  settings,

  /// Acount is deleted.
  deleted,
}
